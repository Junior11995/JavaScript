// Tipos de datos en JavaScript
// Nota para saber el tipo de dato de una variable se usa el typeof en consola 

// Todos tipos de datos numericos se engloban en el tipo de dato number
let Mientero = 10; // entero 
console.log(Mientero); 
console.log(typeof Mientero);

let miFlotante = 2.5;// Decimal o punto flotante
console.log(miFlotante);
console.log(typeof miFlotante);

// Tipos de datos cadena string
let miCadena = "HOla mundo";// Tipo string
console.log(miCadena);
console.log(typeof miCadena);

// tipo de dato null 
let miNull = null;// null 
console.log(miNull);
console.log(typeof miNull);

// tipo de dato undefined
let miDato = undefined;
console.log(miDato);
console.log(miDato);

// tipo de dato boleano 
let miBoolean = true;
console.log(miBoolean);
console.log(typeof miBoolean);